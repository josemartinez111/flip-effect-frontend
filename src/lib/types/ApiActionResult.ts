// ---
// Generic API action result type for all backend API calls.
// This provides a consistent response structure across all actions,
// ensuring type safety and preventing data leaks via URL query parameters.
// All API calls use POST requests (even for reads) to maintain privacy.
// ---

// ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

export type ApiActionResult<T> = {
  // --- Indicates if the operation was successful ---
  success: boolean;
  // --- Human-readable message (success or error context) ---
  message?: string;
  // --- Typed data payload (generic for flexibility) ---
  data?: T;
  // --- Error object if operation failed ---
  error?: Error;
};

