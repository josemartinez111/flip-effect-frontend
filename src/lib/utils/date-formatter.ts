import { Constants as EL } from '../constants/constants';
// ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

type DateFormatType = 'long' | 'year';

// ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

export const formatDate = (format: DateFormatType, date?: string): string => {
  switch (format) {
    case 'year':
      return String(new Date().getFullYear());

    case 'long':
    default: {
      if (!date) { return EL.STR_EMPTY; }
      const d = new Date(date);
      if (isNaN(d.getTime())) { return EL.STR_EMPTY; }
      return new Intl.DateTimeFormat('en-US', {
        year: 'numeric',
        month: 'short',
        day: 'numeric',
        hour: 'numeric',
        minute: 'numeric',
      }).format(d);
    }
  }
};

