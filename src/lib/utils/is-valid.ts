// Validate email using a simple regex
export const isValidEmail = (value: string): boolean => {
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  return emailRegex.test(value);
};
// ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

// Validate phone number (10 digits, optionally with separators)
export const isValidPhoneNumber = (value: string): boolean => {
  const cleaned = value.replace(/\D/g, '');
  return cleaned.length === 10;
};

// Format phone number to XXX-XXX-XXXX
export const formatPhoneNumber = (value: string): string => {
  const cleaned = value.replace(/\D/g, '');
  if (cleaned.length !== 10) return value; // Return original if invalid
  return `${ cleaned.slice(0, 3) }-${ cleaned.slice(3, 6) }-${ cleaned.slice(6, 10) }`;
};
// ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

// Ensures the value is a non-negative integer.
export const isPositiveInteger = (val: string | number | undefined): boolean => {
  const str = String(val).trim();
  if (!str) return false;
  const n = Math.floor(Number(str));
  return n !== Infinity && String(n) === str && n >= 0;
};
