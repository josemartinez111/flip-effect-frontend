import { tryCatchHandler } from '../../../lib';
import { SupabaseContext } from '../../context/supabase-context';

// ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

export type UpdatePasswordResultType = {
  success: boolean;
  message?: string;
};

// ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

export async function updatePasswordAction(
  newPassword: string,
  redirectUrl?: string,
): Promise<UpdatePasswordResultType> {
  const supabase = SupabaseContext.getClient();
  
  return await tryCatchHandler<UpdatePasswordResultType>({
    asyncActionCallback: async () => {
      const { error } = await supabase.auth.updateUser({ password: newPassword });
      if (error) {
        return { success: false, message: error.message };
      }
      if (redirectUrl) {
        window.location.assign(redirectUrl);
      }
      return { success: true, message: 'Password updated successfully.' };
    },
    errorContext: 'Error updating password',
  }) ?? { success: false, message: 'Unknown error during password updateRecipients.' };
}

