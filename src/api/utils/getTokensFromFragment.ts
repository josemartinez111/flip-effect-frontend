// -- Helpers: Parse tokens from URL fragment --
export function getTokensFromFragment(): { access_token?: string; refresh_token?: string } {
  // Remove the leading # & extract the hash part of the URL (after the #)
  const hash = window.location.hash.slice(1);
  // The URLSearchParams API provides read and write access to the query of a URL
  const params = new URLSearchParams(hash);
	
	const result = {
		access_token: params.get('access_token') || undefined,
		refresh_token: params.get('refresh_token') || undefined,
	};
	
	return result;
}

